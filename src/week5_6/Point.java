package week5_6;

public class Point {
    private   int x,y;

    /**
     *
     * @return trả về toạ độ theo x
     */
    public int getX() {
        return x;
    }

    /**
     *
     * @return trả về toạ độ theo y
     */
    public int getY() {
        return y;
    }

    /**
     * đặt toạ độ theo x
     * @param x toạ độ theo x
     */
    public void setX(int x) {
        this.x = x;
    }

    /**
     * đặt toạ độ theo y
     * @param y toạ độ theo y
     */
    public void setY(int y) {
        this.y = y;
    }
}
